        # –û—Ç–∫–∞—Ç—ã–≤–∞–µ–º —Ç–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ
        cursor.execute("UPDATE place SET additional_images = '[]' WHERE additional_images = ?", (test_data,))
